    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/Universidad", "root", "admin");
            PreparedStatement pst = cn.prepareStatement("select * from Estudiante where ID_Estudiante = ?");
            pst.setString(1, txt_buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txt_idestudiante.setText(rs.getString("ID_Estudiante"));
                txt_nombre.setText(rs.getString("Nombre_Estudiante"));
                txt_apellido.setText(rs.getString("Apellido_Estudiante"));
                txt_direccion.setText(rs.getString("Direccion"));
                txt_telefono.setText(rs.getString("Telefono"));
                txt_estado.setText(rs.getString("Estado_Alumno"));
                
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no registrado.");
            }
            
        }catch (Exception e){
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite modificar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();
            
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/Universidad", "root", "admin");
            PreparedStatement pst = cn.prepareStatement("update Estudiante set ID_Estudiante = ?, Nombre_Estudiante=?, Apellido_Estudiante=?, Direccion=?, Telefono=?, Estado_Alumno = ? where ID_Estudiante = " + ID);
            
            pst.setString(1, txt_idestudiante.getText().trim());
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_apellido.getText().trim());
            pst.setString(4, txt_direccion.getText().trim());
            pst.setString(5, txt_telefono.getText().trim());
            pst.setString(6, txt_estado.getText().trim());
            pst.executeUpdate();
            
            label_status.setText("Modificación exitosa.");
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

