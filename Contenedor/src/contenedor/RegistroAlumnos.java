        //Codigo que permite insertar registros en al base de datos
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/Universidad", "root", "admin");
            PreparedStatement pst = cn.prepareStatement("insert into Estudiante values(?,?,?,?,?,?,?)");
            
            pst.setString(1, txt_idestudiante.getText().trim());
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_apellido.getText().trim());
            pst.setString(4, txt_direccion.getText().trim());
            pst.setString(5, txt_telefono.getText().trim());
            pst.setString(6, txt_estado.getText().trim());
            pst.setString(7, txt_maestro.getText().trim());
            
            pst.executeUpdate();
            
            txt_idestudiante.setText("");
            txt_nombre.setText("");
            txt_apellido.setText("");
            txt_direccion.setText("");
            txt_telefono.setText("");
            txt_estado.setText("");
            txt_maestro.setText("");
            
            label_status.setText("Registro exitoso.");
        }catch (Exception e){
            
        }
        
